namespace Application.DTOs
{
    /// <summary>
    /// Data Transfer Object для подозрительной активности в системе.
    /// </summary>
    /// <remarks>
    /// <para>
    /// Представляет информацию о подозрительных или потенциально опасных действиях,
    /// обнаруженных системой мониторинга безопасности.
    /// </para>
    /// <para>
    /// Используется для передачи информации о событиях безопасности между слоями приложения,
    /// логирования и отображения в административных интерфейсах мониторинга.
    /// </para>
    /// </remarks>
    public class SuspiciousActivityDto
    {
        /// <summary>
        /// Тип подозрительной активности.
        /// </summary>
        /// <value>
        /// Строка, содержащая категорию или тип обнаруженной подозрительной активности.
        /// Примеры: "MultipleFailedLoginAttempts", "UnauthorizedAccessAttempt", "DataExfiltrationAttempt".
        /// Используется для классификации и группировки событий безопасности.
        /// </value>
        public string ActivityType { get; set; }

        /// <summary>
        /// Описание подозрительной активности.
        /// </summary>
        /// <value>
        /// Строка, содержащая детальное описание обнаруженной подозрительной активности.
        /// Включает контекст, параметры и обстоятельства события для последующего анализа.
        /// </value>
        public string Description { get; set; }

        /// <summary>
        /// Логин вовлеченного пользователя.
        /// </summary>
        /// <value>
        /// Строка, содержащая логин пользователя, связанного с подозрительной активностью.
        /// Может быть null, если активность не связана с конкретным пользователем или пользователь не аутентифицирован.
        /// </value>
        public string InvolvedLogin { get; set; }

        /// <summary>
        /// Идентификатор вовлеченного пользователя.
        /// </summary>
        /// <value>
        /// GUID пользователя, связанного с подозрительной активностью.
        /// Может быть null, если активность не связана с конкретным пользователем или пользователь не аутентифицирован.
        /// </value>
        public Guid? InvolvedUserId { get; set; }

        /// <summary>
        /// Время последней зафиксированной активности.
        /// </summary>
        /// <value>
        /// Дата и время, когда была зафиксирована подозрительная активность.
        /// Используется для временного анализа и определения последовательности событий.
        /// </value>
        public DateTime LastActivityAt { get; set; }
    }
}