namespace Application.DTOs.Users
{
    /// <summary>
    /// DTO для обновления ролей пользователя
    /// </summary>
    /// <remarks>
    /// Используется в API endpoints для управления назначением ролей пользователю.
    /// Позволяет полностью заменить список ролей, назначенных пользователю.
    /// 
    /// **Важно:** Переданный список полностью заменяет текущие роли пользователя.
    /// Для полного снятия всех ролей передается пустой массив.
    /// 
    /// **Пример использования для назначения новых ролей:**
    /// ```json
    /// {
    ///     "roleIds": [
    ///         "c3d4e5f6-3456-7890-1101-cdef12345678",
    ///         "e5f6a7b8-5678-9012-3101-ef1234567890"
    ///     ]
    /// }
    /// ```
    /// 
    /// **Пример использования для снятия всех ролей:**
    /// ```json
    /// {
    ///     "roleIds": []
    /// }
    /// ```
    /// 
    /// **Безопасность:**
    /// - Изменение ролей может повлиять на уровень доступа пользователя
    /// - Рекомендуется проверка прав текущего пользователя на управление ролями
    /// </remarks>
    public class UpdateUserRolesDto
    {
        /// <summary>
        /// Новый список идентификаторов ролей для пользователя
        /// </summary>
        /// <example>["c3d4e5f6-3456-7890-1101-cdef12345678", "e5f6a7b8-5678-9012-3101-ef1234567890"]</example>
        public List<Guid> RoleIds { get; set; }
    }
}
