namespace Application.DTOs.Users
{
    /// <summary>
    /// DTO для установки статуса активности пользователя
    /// </summary>
    /// <remarks>
    /// Используется в API endpoints для активации/деактивации учетной записи пользователя.
    /// Позволяет управлять доступом пользователя к системе без удаления учетной записи.
    /// **Бизнес-логика:**
    /// - При `isActive: false` пользователь не может войти в систему
    /// - При `isActive: true` доступ восстанавливается
    /// - Деактивированные пользователи сохраняются в системе с историей данных
    /// </remarks>
    public class SetUserStatusDto
    {
        /// <summary>
        /// Флаг активности пользователя
        /// </summary>
        /// <example>true</example>
        public bool IsActive { get; set; }
    }
}
